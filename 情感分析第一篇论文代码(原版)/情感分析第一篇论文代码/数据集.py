def expand_dataset(sentences: List[str], word_map: Dict[str, List[str]], expansion_factor: int = 2) -> List[str]:
    """
    扩展句子数据集，通过词汇替换生成新句子
    
    参数:
    sentences: 原始句子列表
    word_map: 词汇替换字典
    expansion_factor: 扩展倍数
    
    返回:
    扩展后的句子列表
    """
    expanded_sentences = sentences.copy()
    
    for _ in range(expansion_factor - 1):
        for sentence in sentences:
            new_sentence = sentence
            
            # 按长度降序排序词汇，确保长词汇先被替换
            sorted_words = sorted(word_map.keys(), key=len, reverse=True)
            
            for word in sorted_words:
                if word in new_sentence:
                    # 随机选择一个替换词汇
                    replacement = random.choice(word_map[word])
                    new_sentence = new_sentence.replace(word, replacement, 1)  # 只替换第一次出现的词汇
            
            # 确保不添加重复句子
            if new_sentence not in expanded_sentences:
                expanded_sentences.append(new_sentence)
    
    return expanded_sentences

def create_opposite_sentence(positive_sentence: str, positive_words: Dict[str, List[str]], negative_words: Dict[str, List[str]]) -> str:
    """
    根据正向句子生成对应的负向句子
    
    参数:
    positive_sentence: 正向情感句子
    positive_words: 正向情感词汇字典
    negative_words: 负向情感词汇字典
    
    返回:
    对应的负向情感句子
    """
    # 找出句子中的正向词汇
    positive_matches = []
    for word, synonyms in positive_words.items():
        if word in positive_sentence:
            positive_matches.append(word)
    
    # 如果没有找到正向词汇，返回原句
    if not positive_matches:
        return positive_sentence
    
    # 随机选择一个正向词汇进行替换
    word_to_replace = random.choice(positive_matches)
    
    # 构建负向场景（简化版，实际应用中可更复杂）
    negative_scene = f"糟糕的{word_to_replace.replace('阳光', '天气')}"
    
    # 生成负向句子（这里是简化示例，实际应更智能）
    negative_sentence = positive_sentence.replace(word_to_replace, negative_scene, 1)
    
    # 对句子中的其他词汇进行负向替换
    for word, synonyms in negative_words.items():
        if word in negative_sentence:
            replacement = random.choice(synonyms)
            negative_sentence = negative_sentence.replace(word, replacement, 1)
    
    return negative_sentence

def enhance_negative_dataset(positive_sentences: List[str], negative_sentences: List[str], 
                            positive_words: Dict[str, List[str]], negative_words: Dict[str, List[str]],
                            expansion_factor: int = 2) -> List[str]:
    """
    通过正向句子生成对应的负向句子来扩展负向数据集
    
    参数:
    positive_sentences: 正向情感句子列表
    negative_sentences: 负向情感句子列表
    positive_words: 正向情感词汇字典
    negative_words: 负向情感词汇字典
    expansion_factor: 扩展倍数
    
    返回:
    扩展后的负向句子列表
    """
    enhanced_negatives = negative_sentences.copy()
    needed_count = len(positive_sentences) * (expansion_factor - 1)
    
    # 当需要生成的负向句子数量大于现有正向句子时，允许重复使用正向句子
    positive_pool = positive_sentences * (needed_count // len(positive_sentences) + 1)
    
    for i in range(needed_count):
        if i >= len(positive_pool):
            break
            
        positive_sentence = positive_pool[i]
        negative_sentence = create_opposite_sentence(positive_sentence, positive_words, negative_words)
        
        # 确保不添加重复句子
        if negative_sentence not in enhanced_negatives:
            enhanced_negatives.append(negative_sentence)
            
        # 达到目标数量时停止
        if len(enhanced_negatives) >= len(negative_sentences) * expansion_factor:
            break
            
    # 如果生成的数量不足，使用词汇替换方法补充
    if len(enhanced_negatives) < len(negative_sentences) * expansion_factor:
        enhanced_negatives = expand_dataset(negative_sentences, negative_words, expansion_factor)
    
    return enhanced_negatives

